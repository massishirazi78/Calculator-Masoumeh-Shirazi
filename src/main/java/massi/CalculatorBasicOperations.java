package massi;

/*
 * I den klass finns 4 metoder av Basic Operation plus,minus,multiply och division.
 * 
 * 
 */

public class CalculatorBasicOperations {

	/*
	 * Den metod lägga till FirstNumber och SecondNmber sedan return result integer värde av FirstNumber + SecondNmber.
	 * @param FirstNumber  integer
	 * @param SecondNmber  integer
	 * @param return  integer värde av resultat from FirstNumber + SecondNmber
	 */
	
	public int addition(int FirstNumber, int SecondNmber){
		return FirstNumber+SecondNmber;
	}
	/*
	 * Den metod lägga till FirstNumber och SecondNmber sedan return result double värde av FirstNumber + SecondNmber.
	 * @param FirstNumber  double
	 * @param SecondNmber  double
	 * @param return  double värde av resultat from FirstNumber + SecondNmber
	 */

	public double addition(double FirstNumber, double SecondNmber){
		return FirstNumber+SecondNmber;
	}
	/*
	 * Den metod lägga till FirstNumber och SecondNmber sedan return result short värde av FirstNumber + SecondNmber.
	 * @param FirstNumber  short
	 * @param SecondNmber  short
	 * @param return  short värde av resultat from FirstNumber + SecondNmber
	 */
	public short addition(short FirstNumber, short SecondNmber){
		return(short)( FirstNumber + SecondNmber);
	}
	/*
	 * Den metod lägga till FirstNumber och SecondNmber sedan return result long värde av FirstNumber + SecondNmber.
	 * @param FirstNumber  long
	 * @param SecondNmber  long
	 * @param return  long värde av resultat from FirstNumber + SecondNmber
	 */
	
	public long addition(long FirstNumber, long SecondNmber){
		return FirstNumber+SecondNmber;
	}
	
	/*
	 * Den metod minus FirstNumber och SecondNmber sedan return result integer värde av FirstNumber-SecondNmber.
	 * @param FirstNumber  integer
	 * @param SecondNmber  integer
	 * @param return  integer värde av resultat from FirstNumber - SecondNmber
	 */
	
	public int subtraction (int FirstNumber, int SecondNmber){
		return FirstNumber - SecondNmber ;
		
	}
	/*
	 * Den metod minus FirstNumber och SecondNmber sedan return result short värde av FirstNumber-SecondNmber.
	 * @param FirstNumber  short
	 * @param SecondNmber  short
	 * @param return  short värde av resultat from FirstNumber - SecondNmber
	 */
	public short subtraction( short FirstNumber,short SecondNmber){
		return (short)(FirstNumber - SecondNmber) ;
		
	}
	/*
	 * Den metod minus FirstNumber och SecondNmber sedan return result double värde av FirstNumber-SecondNmber.
	 * @param FirstNumber  double
	 * @param SecondNmber  double
	 * @param return  double värde av resultat from FirstNumber - SecondNmber
	 */
	public double subtraction(double FirstNumber,double SecondNmber){
	 return FirstNumber - SecondNmber ;
	}
	/*
	 * Den metod minus FirstNumber och SecondNmber sedan return result long värde av FirstNumber-SecondNmber.
	 * @param FirstNumber  long
	 * @param SecondNmber  long
	 * @param return  long värde av resultat from FirstNumber - SecondNmber
	 */
	public long subtraction(long FirstNumber, long SecondNmber){
		return  FirstNumber - SecondNmber;
	}
	/*
	 * Den metod gånger FirstNumber och snum sedan return result integer värde av FirstNumber*SecondNmber.
	 * @param FirstNumber  integer
	 * @param SecondNmber  integer
	 * @param return  integer värde av resultat from FirstNumber *SecondNmber
	 */
	
	public int multiplikation (int FirstNumber, int SecondNmber){
		return FirstNumber * SecondNmber ;
		
	}
	/*
	 * Den metod gånger FirstNumber och SecondNmber sedan return result short värde av FirstNumber*SecondNmber.
	 * @param FirstNumber  short
	 * @param SecondNmber  short
	 * @param return  short värde av resultat from FirstNumber *SecondNmber
	 */
	public short multiplikation( short FirstNumber,short SecondNmber){
		return (short)(FirstNumber * SecondNmber) ;
		
	}
	/*
	 * Den metod gånger FirstNumber och SecondNmber sedan return result double värde av FirstNumber*SecondNmber.
	 * @param FirstNumber  double
	 * @param SecondNmber  double
	 * @param return  double värde av resultat from FirstNumber *SecondNmber
	 */
	public double multiplikation(double FirstNumber,double SecondNmber){
	 return FirstNumber * SecondNmber ;
	}
	/*
	 * Den metod gånger FirstNumber och SecondNmber sedan return result long värde av FirstNumber*SecondNmber.
	 * @param FirstNumber  long
	 * @param SecondNmber  long
	 * @param return  long värde av resultat from FirstNumber *SecondNmber
	 */
	public long multiplikation(long FirstNumber, long SecondNmber){
		return  FirstNumber * SecondNmber;
	}
	/*
	 * Den metod dela FirstNumber och SecondNmber sedan return result integer värde av FirstNumber/SecondNmber.
	 * @param FirstNumber  integer
	 * @param SecondNmber  integer
	 * @param return  integer värde av resultat from FirstNumber /SecondNmber
	 */
	
	public int dividera (int FirstNumber, int SecondNmber){
		return FirstNumber / SecondNmber ;
		
	}
	/*
	 * Den metod dela FirstNumber och SecondNmber sedan return result short värde av FirstNumber/SecondNmber.
	 * @param FirstNumber  short
	 * @param SecondNmber  short
	 * @param return  short värde av resultat from FirstNumber /SecondNmber
	 */
	public short dividera( short FirstNumber,short SecondNmber){
		return (short)(FirstNumber / SecondNmber) ;
		
	}
	/*
	 * Den metod dela FirstNumber och SecondNmber sedan return result double värde av FirstNumber/SecondNmber.
	 * @param FirstNumber  double
	 * @param SecondNmber  double
	 * @param return  double värde av resultat from FirstNumber /SecondNmber.
	 */
	public double dividera(double FirstNumber,double SecondNmber){
	 return FirstNumber / SecondNmber ;
	}
	/*
	 * Den metod dela FirstNumber och SecondNmber sedan return result long värde av FirstNumber/SecondNmber.
	 * @param FirstNumber  long
	 * @param SecondNmber  long
	 * @param return  long värde av resultat from FirstNumber /SecondNmber
	 */
	public long dividera(long FirstNumber, long SecondNmber){
		return  FirstNumber / SecondNmber;
	}
	
	
}
